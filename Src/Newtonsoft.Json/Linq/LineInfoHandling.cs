namespace Newtonsoft.Json.Linq
{
    /// <summary>
    /// Specifies how line information is handled when loading JSON.
    /// </summary>
    public enum LineInfoHandling
    {
        /// <summary>
        /// Ignore line information.
        /// </summary>
        Ignore = 0,

        /// <summary>
        /// Load line information.
        /// </summary>
        Load = 1
    }
}